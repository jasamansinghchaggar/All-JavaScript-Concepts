// Different types of variable declaration
// 1. const - to store a value that will not be changed in future
// 2. let - used for local declaration (mostly used)
// 3. var - used for global declaration (don't use it)
// *note - variable can be declared also without using any keyword

const PI = 3.14
let post = "Developer"
var myName = "Jasaman Singh"
myId = 123

console.log(PI, post, myName, myId)

console.table([PI, post, myName, myId]) //This method is used to show data in tabular form